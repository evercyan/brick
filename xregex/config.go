// patternMap...
var patternMap = make(map[string]*patternRegexp)

// patternRegexp ...
type patternRegexp struct {
	Is  *regexp.Regexp
	Has *regexp.Regexp
}

// register ...
func register(patterns ...string) {
	for _, pattern := range patterns {
		patternMap[pattern] = &patternRegexp{
			Is:  regexp.MustCompile(`^` + pattern + `$`),
			Has: regexp.MustCompile(pattern),
		}
	}
	return
}

func init() {
	register(
		patternIPV4,
		patternIPV6,
		patternMacAddress,
		patternLink,
		patternEmail,
		patternPhone,
		patternDate,
		patternTime,
		patternChinese,
		patternHexColor,
		patternIdcard,
	)
}

// is ...
func is(pattern, s string) bool {
	return patternMap[pattern].Is.MatchString(s)
}

// has ...
func has(pattern, s string) bool {
	return patternMap[pattern].Has.MatchString(s)
}

// match ...
func match(pattern, s string) []string {
	return patternMap[pattern].Has.FindAllString(s, -1)
}
