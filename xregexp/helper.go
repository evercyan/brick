// is ...
func is(pattern, s string) bool {
	return regexp.MustCompile(`^` + pattern + `$`).MatchString(s)
}

// has ...
func has(pattern, s string) bool {
	return regexp.MustCompile(pattern).MatchString(s)
}

// match ...
func match(pattern, s string) []string {
	return regexp.MustCompile(pattern).FindAllString(s, -1)
}
